#include "libft.h"

int	ft_get_words(char const *s, char c)
{
	int	in;
	int	w;

	w = 0;
	in = 0;
	if (!s)
		return (0);
	while (*s)
	{
		if (*s != c)
		{
			if (in == 0)
			{
				in = 1;
				w++;
			}
		}
		if (*s == c)
		{
			if (in == 1)
				in = 0;
		}
		s++;
	}
	return (w);
}

#include "libft.h"

int	ft_atoi(const char *str)
{
	int						sign;
	unsigned long long int	res;

	sign = 1;
	res = 0;
	while (ft_isspace(*str))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (ft_isdigit(*str))
	{
		res = res * 10 + (*str - '0');
		str++;
	}
	if (res > 9223372036854775807)
		return (sign == 1 ? -1 : 0);
	return (res * sign);
}

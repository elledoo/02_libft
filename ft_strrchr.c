#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*d;
	int		i;

	i = 0;
	d = NULL;
	while (*s)
	{
		if (*s == (char)c)
		{
			d = (char *)s;
			i = 1;
		}
		s++;
	}
	if (i == 0 && c == '\0')
		d = (char *)(s);
	return (d);
}
